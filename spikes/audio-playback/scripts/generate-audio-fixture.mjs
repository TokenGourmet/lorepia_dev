import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { isDeepStrictEqual } from "node:util";

export const FIXTURE_SPEC = Object.freeze({
  protocolVersion: "m1-audio-fixture-v1",
  fixtureId: "m1-audio-v1",
  file: "m1-audio-v1.wav",
  sourcePath: "static/fixtures/m1-audio-v1.wav",
  publicPath: "/fixtures/m1-audio-v1.wav",
  origin: "self-authored",
  license: "CC0-1.0",
  format: "wav-pcm-s16le",
  durationMs: 12_000,
  sampleRateHz: 48_000,
  channels: 1,
  bitsPerSample: 16,
  frameCount: 576_000,
  dataBytes: 1_152_000,
  bytes: 1_152_044,
  seekCheckpointMs: 6_000,
  fadeMs: 20,
  peakAmplitude: 8_192,
  segments: Object.freeze([
    Object.freeze({ startMs: 0, endMs: 3_000, frequencyHz: 440 }),
    Object.freeze({ startMs: 3_000, endMs: 6_000, frequencyHz: 660 }),
    Object.freeze({ startMs: 6_000, endMs: 9_000, frequencyHz: 880 }),
    Object.freeze({ startMs: 9_000, endMs: 12_000, frequencyHz: 1_100 }),
  ]),
});

export const EXPECTED_SHA256 =
  "8559ab4de943a983094b3e27af499ee5fbff80d48263a36fa3c0d1e1339ead25";

const SCRIPT_DIRECTORY = dirname(fileURLToPath(import.meta.url));
export const SPIKE_DIRECTORY = resolve(SCRIPT_DIRECTORY, "..");
export const STATIC_FIXTURE_PATH = resolve(
  SPIKE_DIRECTORY,
  FIXTURE_SPEC.sourcePath,
);
export const CATALOG_PATH = resolve(SPIKE_DIRECTORY, "fixtures/catalog.json");

const WAV_HEADER_BYTES = 44;
const RIFF_FORMAT_CHUNK_BYTES = 16;
const PCM_FORMAT_CODE = 1;
const PHASE_STEPS = 256;
const HALF_CYCLE_STEPS = PHASE_STEPS / 2;

function roundPositiveRatio(numerator, denominator) {
  return Math.floor((numerator + Math.floor(denominator / 2)) / denominator);
}

/**
 * Deterministic sine-like sample using Bhaskara I's rational approximation.
 * It deliberately avoids Math.sin so generated bytes do not depend on libm.
 */
function approximateSineSample(phase) {
  const negative = phase >= HALF_CYCLE_STEPS;
  const halfCyclePhase = negative ? phase - HALF_CYCLE_STEPS : phase;
  const product = halfCyclePhase * (HALF_CYCLE_STEPS - halfCyclePhase);
  const numerator = 16 * product * FIXTURE_SPEC.peakAmplitude;
  const denominator =
    5 * HALF_CYCLE_STEPS * HALF_CYCLE_STEPS - 4 * product;
  const magnitude = roundPositiveRatio(numerator, denominator);
  return negative ? -magnitude : magnitude;
}

function applyFade(sample, frameInSegment, framesPerSegment, fadeFrames) {
  const gainNumerator = Math.min(
    frameInSegment,
    framesPerSegment - 1 - frameInSegment,
    fadeFrames,
  );
  const magnitude = roundPositiveRatio(
    Math.abs(sample) * gainNumerator,
    fadeFrames,
  );
  return sample < 0 ? -magnitude : magnitude;
}

export function generateFixtureBytes() {
  const bytesPerSample = FIXTURE_SPEC.bitsPerSample / 8;
  const blockAlign = FIXTURE_SPEC.channels * bytesPerSample;
  const byteRate = FIXTURE_SPEC.sampleRateHz * blockAlign;
  const framesPerSegment = FIXTURE_SPEC.frameCount / FIXTURE_SPEC.segments.length;
  const fadeFrames =
    (FIXTURE_SPEC.sampleRateHz * FIXTURE_SPEC.fadeMs) / 1_000;

  if (!Number.isInteger(framesPerSegment) || !Number.isInteger(fadeFrames)) {
    throw new Error("Fixture timing must resolve to whole PCM frames");
  }

  const output = Buffer.alloc(FIXTURE_SPEC.bytes);
  output.write("RIFF", 0, 4, "ascii");
  output.writeUInt32LE(36 + FIXTURE_SPEC.dataBytes, 4);
  output.write("WAVE", 8, 4, "ascii");
  output.write("fmt ", 12, 4, "ascii");
  output.writeUInt32LE(RIFF_FORMAT_CHUNK_BYTES, 16);
  output.writeUInt16LE(PCM_FORMAT_CODE, 20);
  output.writeUInt16LE(FIXTURE_SPEC.channels, 22);
  output.writeUInt32LE(FIXTURE_SPEC.sampleRateHz, 24);
  output.writeUInt32LE(byteRate, 28);
  output.writeUInt16LE(blockAlign, 32);
  output.writeUInt16LE(FIXTURE_SPEC.bitsPerSample, 34);
  output.write("data", 36, 4, "ascii");
  output.writeUInt32LE(FIXTURE_SPEC.dataBytes, 40);

  for (let frame = 0; frame < FIXTURE_SPEC.frameCount; frame += 1) {
    const segmentIndex = Math.floor(frame / framesPerSegment);
    const frameInSegment = frame % framesPerSegment;
    const frequencyHz = FIXTURE_SPEC.segments[segmentIndex].frequencyHz;
    const cycleFrame = (frameInSegment * frequencyHz) % FIXTURE_SPEC.sampleRateHz;
    const phase = Math.floor(
      (cycleFrame * PHASE_STEPS) / FIXTURE_SPEC.sampleRateHz,
    );
    const sample = applyFade(
      approximateSineSample(phase),
      frameInSegment,
      framesPerSegment,
      fadeFrames,
    );
    output.writeInt16LE(sample, WAV_HEADER_BYTES + frame * blockAlign);
  }

  return output;
}

export function sha256(bytes) {
  return createHash("sha256").update(bytes).digest("hex");
}

function expectedCatalog() {
  return {
    protocolVersion: FIXTURE_SPEC.protocolVersion,
    fixtureId: FIXTURE_SPEC.fixtureId,
    file: FIXTURE_SPEC.file,
    sourcePath: FIXTURE_SPEC.sourcePath,
    publicPath: FIXTURE_SPEC.publicPath,
    origin: FIXTURE_SPEC.origin,
    license: FIXTURE_SPEC.license,
    sha256: EXPECTED_SHA256,
    bytes: FIXTURE_SPEC.bytes,
    format: FIXTURE_SPEC.format,
    durationMs: FIXTURE_SPEC.durationMs,
    sampleRateHz: FIXTURE_SPEC.sampleRateHz,
    channels: FIXTURE_SPEC.channels,
    bitsPerSample: FIXTURE_SPEC.bitsPerSample,
    frameCount: FIXTURE_SPEC.frameCount,
    dataBytes: FIXTURE_SPEC.dataBytes,
    seekCheckpointMs: FIXTURE_SPEC.seekCheckpointMs,
    fadeMs: FIXTURE_SPEC.fadeMs,
    peakAmplitude: FIXTURE_SPEC.peakAmplitude,
    segments: FIXTURE_SPEC.segments.map((segment) => ({ ...segment })),
  };
}

function readCatalog() {
  let contents;
  try {
    contents = readFileSync(CATALOG_PATH, "utf8");
  } catch (error) {
    throw new Error(`Unable to read fixture catalog at ${CATALOG_PATH}`, {
      cause: error,
    });
  }
  try {
    return JSON.parse(contents);
  } catch (error) {
    throw new Error(`Fixture catalog is not valid JSON: ${CATALOG_PATH}`, {
      cause: error,
    });
  }
}

export function verifySourceFixture() {
  const generated = generateFixtureBytes();
  const generatedSha256 = sha256(generated);
  if (generated.length !== FIXTURE_SPEC.bytes) {
    throw new Error(
      `Generator produced ${generated.length} bytes; expected ${FIXTURE_SPEC.bytes}`,
    );
  }
  if (generatedSha256 !== EXPECTED_SHA256) {
    throw new Error(
      `Generator SHA-256 ${generatedSha256} does not match pinned ${EXPECTED_SHA256}`,
    );
  }

  let committed;
  try {
    committed = readFileSync(STATIC_FIXTURE_PATH);
  } catch (error) {
    throw new Error(`Fixture is missing at ${STATIC_FIXTURE_PATH}`, {
      cause: error,
    });
  }
  if (!committed.equals(generated)) {
    throw new Error(
      `Committed fixture differs from deterministic generator output: ${STATIC_FIXTURE_PATH}`,
    );
  }

  const catalog = readCatalog();
  if (!isDeepStrictEqual(catalog, expectedCatalog())) {
    throw new Error(
      `Fixture catalog does not match the pinned schema and values: ${CATALOG_PATH}`,
    );
  }

  return Object.freeze({
    bytes: committed.length,
    sha256: generatedSha256,
    fixturePath: STATIC_FIXTURE_PATH,
  });
}

function runCli() {
  const args = process.argv.slice(2);
  if (args.length === 0) {
    const output = generateFixtureBytes();
    mkdirSync(dirname(STATIC_FIXTURE_PATH), { recursive: true });
    writeFileSync(STATIC_FIXTURE_PATH, output);
    process.stdout.write(
      `generated ${FIXTURE_SPEC.sourcePath}: ${output.length} bytes sha256 ${sha256(output)}\n`,
    );
    return;
  }
  if (args.length === 1 && args[0] === "--check") {
    const result = verifySourceFixture();
    process.stdout.write(
      `verified ${FIXTURE_SPEC.sourcePath}: ${result.bytes} bytes sha256 ${result.sha256}\n`,
    );
    return;
  }
  throw new Error("Usage: node scripts/generate-audio-fixture.mjs [--check]");
}

const invokedPath = process.argv[1] ? resolve(process.argv[1]) : "";
if (invokedPath === fileURLToPath(import.meta.url)) {
  try {
    runCli();
  } catch (error) {
    process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
    process.exitCode = 1;
  }
}
