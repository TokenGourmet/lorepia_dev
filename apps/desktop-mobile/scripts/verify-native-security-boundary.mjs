const CORE_WINDOW_PERMISSIONS = new Set(["core:window:allow-destroy"]);
const EXACT_CSP = Object.freeze({
  "default-src": "'self' customprotocol: asset:",
  "base-uri": "'none'",
  "connect-src": "'self'",
  "font-src": "'self'",
  "form-action": "'none'",
  "frame-src": "'none'",
  "img-src": "'none'",
  "media-src": "'none'",
  "object-src": "'none'",
  "script-src": "'self'",
  "style-src": "'self'",
  "worker-src": "'none'",
});

function sorted(values) {
  return [...values].sort((left, right) => left.localeCompare(right));
}

function assertSameSet(actual, expected, code) {
  const left = sorted(actual);
  const right = sorted(expected);
  if (JSON.stringify(left) !== JSON.stringify(right)) {
    throw new Error(`${code}:expected=${JSON.stringify(right)} actual=${JSON.stringify(left)}`);
  }
}

export function parseProductCommands(source) {
  const body = source.match(/macro_rules!\s+with_product_app_commands[\s\S]*?\$macro!\(([^)]*)\)/u)?.[1];
  if (!body) throw new Error("PRODUCT_COMMAND_MACRO_NOT_FOUND");
  const commands = body
    .split(",")
    .map((command) => command.trim())
    .filter(Boolean);
  if (new Set(commands).size !== commands.length) throw new Error("DUPLICATE_PRODUCT_COMMAND");
  return commands;
}

export function verifySecurityContracts({ commandSource, capability, tauriConfig, permissionFiles }) {
  const commands = parseProductCommands(commandSource);
  assertSameSet(commands, PRODUCT_COMMANDS, "NATIVE_COMMAND_SURFACE_DRIFT");

  if (capability?.identifier !== "default") throw new Error("UNEXPECTED_CAPABILITY_IDENTIFIER");
  if (!Array.isArray(capability.webviews) || capability.webviews.length !== 1 || capability.webviews[0] !== "main") {
    throw new Error("CAPABILITY_MUST_BIND_EXACT_MAIN_WEBVIEW");
  }
  if (Object.hasOwn(capability, "windows")) throw new Error("WINDOW_WILDCARD_OR_ALIAS_NOT_ALLOWED");

  const expectedPermissions = new Set([
    ...CORE_WINDOW_PERMISSIONS,
    ...commands.map((command) => `allow-${command.replaceAll("_", "-")}`),
  ]);
  if (!Array.isArray(capability.permissions)) throw new Error("CAPABILITY_PERMISSIONS_MISSING");
  assertSameSet(capability.permissions, expectedPermissions, "CAPABILITY_PERMISSION_DRIFT");

  const generatedCommands = [];
  for (const [name, source] of Object.entries(permissionFiles)) {
    const allow = source.match(/commands\.allow\s*=\s*\["([a-z0-9_]+)"\]/u)?.[1];
    const deny = source.match(/commands\.deny\s*=\s*\["([a-z0-9_]+)"\]/u)?.[1];
    if (!allow || allow !== deny) throw new Error(`INVALID_GENERATED_PERMISSION:${name}`);
    const expectedName = `${allow}.toml`;
    if (name !== expectedName) throw new Error(`MISNAMED_GENERATED_PERMISSION:${name}`);
    generatedCommands.push(allow);
  }
  assertSameSet(generatedCommands, commands, "GENERATED_PERMISSION_DRIFT");

  const security = tauriConfig?.app?.security;
  if (security?.freezePrototype !== true) throw new Error("FREEZE_PROTOTYPE_REQUIRED");
  assertSameSet(security?.capabilities ?? [], ["default"], "TAURI_CAPABILITY_DRIFT");
  const csp = security?.csp;
  if (!csp || typeof csp !== "object" || Array.isArray(csp)) throw new Error("RELEASE_CSP_MISSING");
  assertSameSet(Object.keys(csp), Object.keys(EXACT_CSP), "RELEASE_CSP_DIRECTIVE_DRIFT");
  for (const [directive, expected] of Object.entries(EXACT_CSP)) {
    if (csp[directive] !== expected) {
      throw new Error(`RELEASE_CSP_VALUE_DRIFT:${directive}`);
    }
  }

  return { commands: commands.length, permissions: generatedCommands.length };
}

export function verifyNativeSecurityBoundary(tauriRoot) {
  const permissionDirectory = resolve(tauriRoot, "permissions/autogenerated");
  const permissionFiles = Object.fromEntries(
    readdirSync(permissionDirectory)
      .filter((name) => name.endsWith(".toml"))
      .map((name) => [basename(name, ".toml") + ".toml", readFileSync(resolve(permissionDirectory, name), "utf8")]),
  );
  return verifySecurityContracts({
    commandSource: readFileSync(resolve(tauriRoot, "src/app_commands.rs"), "utf8"),
    capability: JSON.parse(readFileSync(resolve(tauriRoot, "capabilities/default.json"), "utf8")),
    tauriConfig: JSON.parse(readFileSync(resolve(tauriRoot, "tauri.conf.json"), "utf8")),
    permissionFiles,
  });
}

const invokedPath = process.argv[1] ? pathToFileURL(resolve(process.argv[1])).href : "";
if (import.meta.url === invokedPath) {
  const scriptDirectory = resolve(fileURLToPath(new URL(".", import.meta.url)));
  const result = verifyNativeSecurityBoundary(resolve(scriptDirectory, "../src-tauri"));
  process.stdout.write(
    `verified native boundary: ${result.commands} commands, exact main WebView, strict release CSP\n`,
  );
}
